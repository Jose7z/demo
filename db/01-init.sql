CREATE TABLE IF NOT EXISTS envanter (
    etiketno VARCHAR(255) PRIMARY KEY,
    durum VARCHAR(255),
    irsaliyetarihi DATE,
    lokasyonadi VARCHAR(255),
    lokasyonkodu VARCHAR(255),
    lokasyontipi VARCHAR(255),
    modeladi VARCHAR(255),
    serialnumber VARCHAR(255),
    sinif VARCHAR(255),
    sorumluluk VARCHAR(255),
    sorumluluksicil VARCHAR(255),
    urunailesi VARCHAR(255)
);